import UIKit

extension UIColor {
    static func mainRedColor() -> UIColor {
        return UIColor.mainRed
    }
    
    static func mainWhiteColor() -> UIColor {
        return UIColor.mainWhite
    }
    
    static func mainDarkColor() -> UIColor {
        return UIColor.mainDark
    }
}

